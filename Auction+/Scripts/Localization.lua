function GetLocalizedText(TextName)
    local ClientLocalization = common.GetLocalization()
    --local ClientLocalization = "eng"
    local LocalizedText
    if not Texts[ClientLocalization] or not Texts[ClientLocalization][TextName] then
        LocalizedText = TextName
    else
        LocalizedText = Texts[ClientLocalization][TextName]
    end
	return LocalizedText
end